#include<iostream>
using namespace std;

int main() {
    int x,y,c,d;
    x=15;
    y=2;
    c=12;
    d=6;
    x +=y;    //x =+y   is x = x+y
    cout<<x<<endl;
    x -=y;     //x =-y   is x = x-y
    cout<<x<<endl;
    x*=y ;     //x =*y   is x = x*y
    cout<<x<<endl;
    c/=d;    //x =/y   is c = c/d
    cout<<c<<endl;


 return 0;
}

